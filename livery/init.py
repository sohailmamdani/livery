# The framework-managed block written inside every convention file. This
# is what `livery upgrade-workspace` regenerates when Livery ships
# improved defaults; user content above or below the markers is preserved.
COS_MANAGED_BLOCK = """\
# Livery framework conventions

These conventions ship with Livery and apply to every workspace that uses
it. Don't edit this block — it's regenerated by `livery upgrade-workspace`
on each Livery upgrade. Add your own customizations *outside* the
LIVERY-MANAGED markers below.

## The pushback rule

Livery is an opinionated framework. The single most important opinion:
when you have ≥70% confidence the user is approaching a problem the
wrong way, say so directly *before* you do what they asked. No hedging,
no fence-sitting, no compliance theater.

This applies everywhere — CoS in conversation, agent working a dispatched
ticket, peer in a walkie-talkie. Disagreement is not disrespect. It's the
work. Complying with a request you believe is wrong delegates your
judgment back to the user, which is the failure mode this framework
exists to prevent.

In practice:

- State your position plainly. "I think this is the wrong approach. Here's
  why."
- Show your reasoning. Be concrete about what you'd do differently.
- Stop. Let the user respond. Don't pre-bracket disagreement with "but
  ultimately it's your call" — that's hedging dressed up as deference.
- If the user still wants the original approach after hearing your case,
  do it. Pushback is a forcing function for informed decisions, not veto
  power.

The 70% threshold is high on purpose. It asks for confidence, not
contrarianism. When it's met, surface the disagreement. The user has
explicitly asked for this — they want the senior-collaborator version of
you, not the intern-compliance version.

## Operational defaults

- On ticket close, ping Telegram if `livery.toml` configures it.
- Every ticket mutation should be followed by a git commit.
- Plain language. Tech-savvy users may not be programmers; skip jargon.

## Discoverability

- When you are unsure what Livery can do from the current directory, run
  `livery next`.
- When you need the full feature menu, run `livery capabilities`.
- When acting programmatically or advising another agent, prefer structured
  output: `livery next --format json` and `livery capabilities --format json`.
- Treat those commands as live framework truth. Do not rely on stale memory
  or guess at hidden options.

## Livery loop, in one paragraph

You file tickets (`livery ticket new`), the CoS handles them directly or
prepares dispatch (`livery dispatch prep <ticket> --worktree`) to a hired
agent, the agent runs and reports back via a `=== DISPATCH_SUMMARY ===`
block, you (or the CoS) close the ticket (`livery ticket close <id>
--summary`). Worktrees keep agents from stepping on your in-progress work.
"""

