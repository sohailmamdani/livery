LIVERY_TOML_HEADER = """# Livery workspace config.
#
# This file marks this directory as a Livery workspace. The CLI walks up
# from your cwd looking for this file. Fields are optional with sensible
# defaults.
"""


def _render_livery_toml(
    *,
    name: str,
    description: str,
    default_runtime: str | None,
    telegram_chat_id: str | None,
    telegram_token_file: str | None,
    cos_engines: list[str],
) -> str:
    """Render a livery.toml. Unset fields are written as commented-out examples."""
    lines = [LIVERY_TOML_HEADER.rstrip(), ""]
    lines.append(f'name = "{name}"')
    lines.append(f'description = "{description}"')
    lines.append("")
    if default_runtime:
        lines.append(f'default_runtime = "{default_runtime}"')
    else:
        lines.append("# Runtime default for some subcommands. Leave unset to force explicit choice.")
        lines.append('# default_runtime = "claude_code"')
    lines.append("")
    quoted_engines = ", ".join(f'"{e}"' for e in cos_engines)
    lines.append("# CoS engines this workspace targets. Used by `livery upgrade-workspace`")
    lines.append("# to know which convention files and skill directories to manage.")
    lines.append(f"cos_engines = [{quoted_engines}]")
    lines.append("")
    lines.append("[telegram]")
    if telegram_chat_id:
        lines.append(f'chat_id = "{telegram_chat_id}"')
    else:
        lines.append('# chat_id = "-1001234567890"       # group or DM id for ticket-close pings')
    if telegram_token_file:
        lines.append(f'token_file = "{telegram_token_file}"')
    else:
        lines.append('# token_file = "~/.claude/channels/telegram/.env"  # path to bot-token .env')
    return "\n".join(lines) + "\n"


